# n = 3
# 2^(3) = 8
#
# 0 -> "000" XOR "000" -> "000"
# 1 -> "001" XOR "000" -> "001"
# 2 -> "010" XOR "001" -> "011"
# 3 -> "011" XOR "001" -> "010"
# 4 -> "100" XOR "010" -> "110"
# 5 -> "101" XOR "010" -> "111"
# 6 -> "110" XOR "011" -> "101"
# 7 -> "111" XOR "011" -> "100"

class Solution:
    def grayCode(self, n: int) -> List[int]:
        return [i ^ (i >> 1) for i in range(2**n)]
